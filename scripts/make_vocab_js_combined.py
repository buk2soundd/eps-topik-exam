    book1 = load_and_clean(ROOT / 'scripts' / 'vocab_data_book1.json')
    book2 = load_and_clean(ROOT / 'scripts' / 'vocab_data_all.json')
    
    all_chapters = book1 + book2
    all_chapters.sort(key=lambda c: c['chapter'])
    
    total_words = sum(len(c['words']) for c in all_chapters)
    print(f"Book 1: {len(book1)} chapters, {sum(len(c['words']) for c in book1)} words")
    print(f"Book 2: {len(book2)} chapters, {sum(len(c['words']) for c in book2)} words")
    print(f"Total: {len(all_chapters)} chapters, {total_words} words")
    
    # Build JS output
