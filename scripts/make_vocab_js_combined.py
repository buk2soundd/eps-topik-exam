"""
Merge Book 1 (ch06-ch30) and Book 2 (ch31-ch59) vocab JSON files
into a single src/data/vocabData.js file with clean data.
"""
import json, re, pathlib, unicodedata

ROOT = pathlib.Path(__file__).parent.parent

def is_lao(text):
    """Check if text contains Lao unicode characters."""
    for ch in text:
        if '\u0E80' <= ch <= '\u0EFF':
            return True
    return False

def is_sentence(text):
    """Heuristic: likely a full sentence rather than a single vocab word."""
    # Long text (>25 chars in Korean) or contains verb endings common in sentences
    if len(text) > 30:
        return True
    # Ends in typical sentence patterns
    if re.search(r'(입니다|이에요|에요|습니다|ㅂ니까|세요|요\.|니다\.)$', text):
        return True
    return False

def clean_meaning(meaning):
    """Remove extra whitespace, newlines."""
    meaning = re.sub(r'\s+', ' ', meaning).strip()
    return meaning

def load_and_clean(json_path):
    with open(json_path, encoding='utf-8') as f:
        chapters = json.load(f)
    
    cleaned = []
    for ch in chapters:
        words = []
        seen_words = set()
        for w in ch['words']:
            word = w['word'].strip()
            meaning = clean_meaning(w.get('meaning', ''))
            img = w.get('img', '')
            
            # Skip empty words
            if not word:
                continue
            # Skip duplicate words in same chapter
            if word in seen_words:
                continue
            # Skip words that are full Korean sentences
            if is_sentence(word):
                continue
            # Skip "(no translation)" or empty meaning unless word itself is useful
            if not meaning or meaning == '(no translation)':
                meaning = ''
            # Skip entries with only English (no Lao) - keep Lao ones
            # But allow words even if meaning is empty (still show the image+word)
            # Just skip if meaning is clearly English-only and not Lao
            if meaning and not is_lao(meaning) and re.match(r'^[A-Za-z\s/\(\)\.,]+$', meaning):
                meaning = ''  # Remove English-only meanings
            
            # Skip items that are grouped label lines (multiple words in one item)
            # e.g. "사과 오렌지 포도 수박" - multiple space-separated Korean words
            parts = word.split()
            if len(parts) >= 4 and all(re.match(r'^[가-힣]+$', p) for p in parts[:4]):
                continue
            
            seen_words.add(word)
            words.append({'word': word, 'meaning': meaning, 'img': img})
        
        if words:
            cleaned.append({
                'chapter': ch['chapter'],
                'title': ch['title'],
                'titleKr': ch.get('titleKr', ''),
                'titleLo': ch.get('titleLo', ''),
                'words': words,
            })
    return cleaned

