# Clean up the @ separators (used to avoid python parsing issues with combining chars)
CLEAN = {k: v.replace("@", "") for k, v in TRANSLATIONS.items()}


def apply_translations():
    with open(VOCAB_PATH, encoding="utf-8") as f:
        data = json.load(f)

    applied = 0
    for chapter in data["chapters"]:
        for word in chapter["words"]:
            en = word.get("english", "")
            lao = word.get("lao", en)
            if lao != en:
                continue  # already translated
            if en in CLEAN:
                new_lao = CLEAN[en]
                if has_lao(new_lao):
                    word["lao"] = new_lao
                    applied += 1
                else:
                    print(f"  SKIP (not enough Lao): {repr(en)} -> {repr(new_lao)}")

    with open(VOCAB_PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    shutil.copy(VOCAB_PATH, DATA_PATH)

    remaining = sum(
        1 for ch in data["chapters"]
        for w in ch["words"]
        if w.get("lao", w.get("english", "")) == w.get("english", "")
    )
    print(f"Applied: {applied}  |  Still English fallback: {remaining}")


if __name__ == "__main__":
    apply_translations()
