# Only apply entries with >= 2 Lao chars OR that are Latin K-* strings
def has_lao(s):
    return sum(1 for c in s if '\u0e80' <= c <= '\u0eff') >= 2

CLEAN = {en: lao for en, lao in KNOWN.items() if has_lao(lao) or lao.startswith("K-") or lao == "TOPIK"}

VOCAB_PATH = "d:/9.15/EPStest/scripts/standard_vocab.json"
DATA_PATH  = "d:/9.15/EPStest/src/data/standardVocabData.json"

with open(VOCAB_PATH, encoding="utf-8") as f:
    data = json.load(f)

added = already = 0
for ch in data["chapters"]:
    for w in ch["words"]:
        en = w.get("english", "")
        cur_lao = w.get("lao", en)
        if cur_lao != en:
            already += 1
            continue
        lao = CLEAN.get(en)
        if lao:
            w["lao"] = lao
            added += 1

with open(VOCAB_PATH, "w", encoding="utf-8") as f:
    json.dump(data, f, ensure_ascii=False, indent=2)
shutil.copy(VOCAB_PATH, DATA_PATH)

print(f"New Lao applied: {added}  |  Already had translation: {already}")
remaining = sum(
    1 for ch in data["chapters"] for w in ch["words"]
    if w.get("lao", w.get("english","")) == w.get("english","")
)
print(f"Still English fallback: {remaining}")
